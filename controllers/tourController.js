const Tour = require('../models/tourModel');

exports.checkBody = (req, res, next) => {
  if (!req.body.name || !req.body.price) {
    return res
      .status(400)
      .json({ status: 'fail', message: 'Missing name or price' });
  }
  next();
};

//♾️
exports.getallTours = async (req, res) => {
  try {
    // 1) Filtering
    const queryObj = { ...req.query };
    const excludeFields = ['page', 'sort', 'limit', 'fields'];
    excludeFields.forEach((el) => delete queryObj[el]);

    // 2) Advanced filtering
    let queryStr = JSON.stringify(queryObj);
    queryStr = queryStr.replace(/\b(gte|gt|lte|lt)\b/g, (match) => `$${match}`);

    let finalQueryObj = JSON.parse(queryStr);

    // Convert numeric strings to numbers
    Object.keys(finalQueryObj).forEach((key) => {
      if (
        typeof finalQueryObj[key] === 'string' &&
        !isNaN(finalQueryObj[key])
      ) {
        finalQueryObj[key] = Number(finalQueryObj[key]);
      }
    });

    // 3) EXECUTE QUERY with the transformed object
    const tours = await Tour.find(finalQueryObj);

    // 4) SEND RESPONSE
    res.status(200).json({
      status: 'success',
      results: tours.length,
      data: { tours },
    });
  } catch (error) {
    res.status(400).json({
      status: 'fail',
      message: error.message,
    });
  }
};

